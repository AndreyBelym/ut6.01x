enum Object_Type{
	PLAYER_SHIP=0,
	ENEMY_SHIP=1,
	LASER=2
};
struct Object{
	const unsigned char *sprite;
	unsigned char w,h,x,y;
	enum Object_Type type;
};

void Object_Init(struct Object* obj,enum Object_Type type,const unsigned char*  sprite) {
	obj->type=type;
	obj->sprite=sprite;
	obj->w=SPRITE_WIDTH(sprite);
	obj->h=SPRITE_HEIGTH(sprite);
	obj->x=0;obj->y=0;
}

void Object_Draw(struct Object* obj){
	if(obj->sprite)
		Nokia5110_PrintBMP(obj->x, obj->y, obj->sprite, 0);
}

struct EnemyObject{
	struct Object base;
	unsigned char laserCountdown;
} SmallEnemy;

struct EnemySystem{
	struct EnemyObject enemies[5],*active_enemies[5];
	int n_active_enemies;
};

void EnemySystem_Init(struct EnemySystem* esys){
	unsigned char i;
	esys->n_active_enemies=5;
	for(i=0;i<5;i++){
		esys->enemies[i]=SmallEnemy;
		esys->enemies[i].laserCountdown=Random()%7+3;
		OBJECT(esys->enemies[i]).x=OBJECT(SmallEnemy).w*i;
		OBJECT(esys->enemies[i]).y=0;
		esys->active_enemies[i]=esys->enemies+i;
	}
}
struct LaserObject{
	struct Object base;
	signed char velocity;
} Laser;

struct LaserSystem{
	struct LaserObject *active_lasers[64];
	unsigned n_active_lasers;
};

void LaserSystem_Init(struct LaserSystem *lsys){
	lsys->n_active_lasers=0;
}

struct Object PlayerShip;

void Objects_Init(){
	Object_Init(pOBJECT(&SmallEnemy),ENEMY_SHIP,SmallEnemy_sprite);
	
	Object_Init(&PlayerShip,PLAYER_SHIP,PlayerShip_sprite);
	
	Object_Init(pOBJECT(&Laser),LASER,Laser_sprite);
}


void SysTick_Init(){
	NVIC_ST_CTRL_R = 0;           // disable SysTick during setup
	NVIC_ST_RELOAD_R = 1333333;
	NVIC_ST_CURRENT_R = 0;
  NVIC_SYS_PRI3_R = NVIC_SYS_PRI3_R&0x80FFFFFF; // priority 4 
  NVIC_ST_CTRL_R = 0x00000007;
}

void LaserSystem_FireLaser(struct LaserSystem *lsys,signed char vel,const struct Object firing){
	if(lsys->n_active_lasers<64){
			struct LaserObject *new_laser=malloc(sizeof(Laser));
			*new_laser=Laser;
		  new_laser->velocity=vel;
			OBJECT(*new_laser).x=firing.x+firing.w/2-OBJECT(*new_laser).w/2;
			if(vel<0)
				OBJECT(*new_laser).y=firing.y-OBJECT(*new_laser).h;
			else
				OBJECT(*new_laser).y=firing.y+firing.h;
			lsys->active_lasers[lsys->n_active_lasers]=new_laser;
			lsys->n_active_lasers++;
		}
}

unsigned LaserSystem_ProcessLasers(struct LaserSystem *lsys,struct EnemySystem* esys, struct Object* pship){
	unsigned char i,j,k;
	unsigned status=0;
	for(i=0;i<lsys->n_active_lasers;i++){
		struct LaserObject *curr_laser=lsys->active_lasers[i];
		signed char vel=curr_laser->velocity;
		if(((vel<0)&&(OBJECT(*curr_laser).y<=-vel))||
				(OBJECT(*curr_laser).y>=48-OBJECT(Laser).h-vel)){
			for(j=i+1;j<lsys->n_active_lasers;j++){
				lsys->active_lasers[j-1]=lsys->active_lasers[j];
			}
			lsys->n_active_lasers--;
			free(curr_laser);
			curr_laser=NULL;
			i--;
		} else{
			OBJECT(*curr_laser).y+=curr_laser->velocity;
			for(j=0;j<esys->n_active_enemies;j++){
				struct EnemyObject *curr_enemy=esys->active_enemies[j];
				if(curr_laser!=NULL
						&&(OBJECT(*curr_enemy).x < OBJECT(*curr_laser).x+OBJECT(*curr_laser).w)
						&&(OBJECT(*curr_enemy).x+OBJECT(*curr_enemy).w > OBJECT(*curr_laser).x)
						&&(pOBJECT(curr_enemy)->y < pOBJECT(curr_laser)->y+pOBJECT(curr_laser)->h)
						&&(OBJECT(*curr_enemy).y+OBJECT(*curr_enemy).h > OBJECT(*curr_laser).y)){
						//COLLISION!
						for(k=j+1;k<esys->n_active_enemies;k++){
							esys->active_enemies[k-1]=esys->active_enemies[k];
						}
						esys->n_active_enemies--;
						j--;
						for(k=i+1;k<lsys->n_active_lasers;k++){
							lsys->active_lasers[k-1]=lsys->active_lasers[k];
						}
						lsys->n_active_lasers--;
						free(curr_laser);
						curr_laser=NULL;
						i--;
						
				}
			}
			if( curr_laser!=NULL
					&&(pship->x < OBJECT(*curr_laser).x+OBJECT(*curr_laser).w)
					&&(pship->x+pship->w > OBJECT(*curr_laser).x)
					&&(pship->y < pOBJECT(curr_laser)->y+pOBJECT(curr_laser)->h)
					&&(pship->y+pship->h > pOBJECT(curr_laser)->y)){
							status=1;
			}
					
		}
	}
	return status;
}

int main(void){
	int i=0; unsigned char dir=0; struct Input in;
	struct LaserSystem laserSys;
	struct EnemySystem enemySys;
	unsigned status=0;
	int Prev_Shot=0;
  TExaS_Init(SSI0_Real_Nokia5110_Scope);  // set system clock to 80 MHz
  Random_Init(1);
  Nokia5110_Init();
	Input_Init();
	Sound_Init();
	SysTick_Init();
	Objects_Init();
	Random_Init(1);
	
	EnemySystem_Init(&enemySys);
	LaserSystem_Init(&laserSys);
	
  Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();      // draw buffer


	PlayerShip.y=48-PlayerShip.h;
  while(1){
		while(!Semaphore);
		Semaphore=0;
		
		in=Input_Read();
		
		PlayerShip.x=in.ADC_Value*(84-PlayerShip.w)/4095;
		
		if(BUTTON_A(in)){
				if(TimerCount-Prev_Shot>=30){
					LaserSystem_FireLaser(&laserSys,-3,PlayerShip);
					Prev_Shot=TimerCount;
				}
				GPIO_PORTB_DATA_R|=0x10;
		} else {
				GPIO_PORTB_DATA_R&=~0x10;
		}
		if(BUTTON_B(in)){
				GPIO_PORTB_DATA_R|=0x20;
		} else {
				GPIO_PORTB_DATA_R&=~0x20;
		}
		
		if(!(TimerCount%5)){
			status=LaserSystem_ProcessLasers(&laserSys,&enemySys,&PlayerShip);
		}
		if(!(TimerCount%60)){
			//Handle Enemies
			
			//Handle Lasers
			if(dir&&(pOBJECT(enemySys.active_enemies[enemySys.n_active_enemies-1])->x+OBJECT(SmallEnemy).w==84))
				dir=0;
			if(!dir&&(pOBJECT(enemySys.active_enemies[0])->x==0))
				dir=1;
			for(i=0;i<enemySys.n_active_enemies;i++){
				if(dir)
					pOBJECT(enemySys.active_enemies[i])->x++;
				else
					pOBJECT(enemySys.active_enemies[i])->x--;
				pOBJECT(enemySys.active_enemies[i])->y++;
				enemySys.active_enemies[i]->laserCountdown--;
				if(enemySys.active_enemies[i]->laserCountdown==0){
					LaserSystem_FireLaser(&laserSys,3,OBJECT(*(enemySys.active_enemies[i])));
					enemySys.active_enemies[i]->laserCountdown=Random()%7+3;
				}
			}
		}
		
		Nokia5110_ClearBuffer();
		
		Object_Draw(&PlayerShip);
		
		for(i=0;i<enemySys.n_active_enemies;i++){
			Object_Draw(pOBJECT(enemySys.active_enemies[i]));
		}
		
		for(i=0;i<laserSys.n_active_lasers;i++){
			Object_Draw(pOBJECT(laserSys.active_lasers[i]));
		}
		
		Nokia5110_DisplayBuffer();
		
		if(status||!enemySys.n_active_enemies||pOBJECT(enemySys.active_enemies[0])->y>=48-PlayerShip.h-OBJECT(SmallEnemy).h)
				break;
  }
	//Nokia5110_Clear();
	if(status||enemySys.n_active_enemies)
		Nokia5110_OutString("GAME OVER!");
	else
		Nokia5110_OutString("YOU WON!");
	for(;;);
}
